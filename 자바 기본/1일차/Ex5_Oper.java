package pm;

public class Ex5_Oper {

	public static void main(String[] args) {
		
		int v1 = 7;
		int v2 = 10;
		
		boolean res =v1 > v2; //v1>v2라면 true 아니라면 false
		System.out.println("v1 > v2 = "+res); //false
		
		res =v1 >= v2; //v1>v2라면 true 아니라면 false
		System.out.println("v1 >= v2 = "+res); //false
		
		res =v1 < v2; //v1>v2라면 true 아니라면 false
		System.out.println("v1 < v2 = "+res); //true
	}

}
